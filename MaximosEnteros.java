public class MaximosEnteros {
    public static void main(String[] args) {
        // Mayor número entero representable por un char
        char maxChar = Character.MAX_VALUE;
        System.out.println("Mayor número entero (char): " + (int) maxChar);

        // Mayor número entero representable por un short
        short maxShort = Short.MAX_VALUE;
        System.out.println("Mayor número entero (short): " + maxShort);

        // Mayor número entero representable por un int
        int maxInt = Integer.MAX_VALUE;
        System.out.println("Mayor número entero (int): " + maxInt);
    }
}
